# Lista de datos string
ciudades = ["Lima", "Bogotá", "Madrid", "Buenos Aires", "Santiago", "Ciudad de México"]

# Lista de datos numéricos
edades = [25, 18, 32, 40, 22, 19, 35, 29, 50, 27]

print("La lista de ciudades es: {}".format(ciudades))
print("La lista de edades es: {}".format(edades))
print("_______________________________________________________")

# Ordenando las listas
ciudades.sort()
edades.sort()

# Listas ordenadas
print("La lista de ciudades ordenada es: {}".format(ciudades))
print("La lista de edades ordenada es: {}".format(edades))
